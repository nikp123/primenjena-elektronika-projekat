void otvorena_garaza_init() { 
    // Upalimo svetlo u garazi   
    LATBbits.LATB7 = 1;
    // Crtamo sliku za otkljucanu garazu
    draw(unlock);
}

bool otvorena_garaza() {
    /** 
      Ideja je sledeca: Kako smo unutar otvorene garaze, vrata se otvaraju.
      Zarad pojednostavljenja smatramo da je stanje ugla 0 otvorena garaza, 
      a stanje ugla 180 zatvorena garaza.

      Princip rada je sledeci:
       - Imamo tajmer koji broji milisedunde od pocetka rada
       - Biramo periodu od 20 milisekundi unutar tog tajmera
         i cekamo da se nasa perioda poklopi sa tajmerom
       - Zatim da bi servo otisao u poziciju nula, visoko stanje
        te periode treba da bude 1 milisekund. Da bi smo podigli signal
        za takvu periodu, ulazimo u beskonacnu petlju gde postavljamo izlaz
        steper motora na visoku vrednost i cekamo da ta milisekunda prodje.
       - Radimo isto samo za nizak dio periode i nastavimo sa logikom tog stanja
    **/
    while(timer_ms() % 20) {}; // cekaj periodu
    while((timer_ms() % 20) < 1) LATBbits.LATB11 = 1; // stvori puls od 1 ms
    while(timer_ms() % 20)       LATBbits.LATB11 = 0;  // cekaj ostatak periode

    // Da li korisnik zeli da zakljuca garazu
    return touchscreen_touch();
}